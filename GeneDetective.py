import argparse


def write_dict_to_file(dictionary, output_file):
    output_lines = [f"{key} {value}\n" for key, value in dictionary.items()]
    with open(output_file, "w") as file:
        file.writelines(output_lines)


def change_dict_key(dict, old_key, new_key, default_value=None):
    dict[new_key] = dict.pop(old_key, default_value)


def extract_to_dictionary(file_path):
    result = {}
    try:
        with open(file_path, "r") as file:
            for line in file:
                if len(line) >= 11:
                    key = line[:10]
                    # Remove leading and trailing whitespaces
                    value = line[11:].strip()
                    result[key] = value
                else:
                    print(f"Skipped line: {line} (insufficient length)")
    except FileNotFoundError:
        print("File not found.")
    return result


def compare_and_replace_dicts(dict1, dict2):
    result_dict = dict1.copy()  # Create a copy of the first dictionary
    keys_set = set(dict1.keys())  # Create a set of keys from dict1
    for key, value in dict2.items():
        if key in keys_set:
            result_dict[key] = value
            new_key = "[x] " + key
            change_dict_key(result_dict, key, new_key)

    return result_dict


def main():
    parser = argparse.ArgumentParser(
        description="GeneDetective requires a primary and secondary file to function, it'll find matching entries on both and replace the values of the matching entries on the secondary with the primary file and output the result to a text files with a desired user path. Changes are also marked with [x].")
    parser.add_argument("file1", help="Path to the first file")
    parser.add_argument("file2", help="Path to the second file")
    parser.add_argument("output", help="Path to the output file")
    args = parser.parse_args()

    dict1 = extract_to_dictionary(args.file1)
    dict2 = extract_to_dictionary(args.file2)
    output = compare_and_replace_dicts(dict1, dict2)

    write_dict_to_file(output, args.output)


if __name__ == "__main__":
    main()
